    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        String nombre;
        int salario_hora, horas_mes, salario_mensual;
        
        nombre = txtNombre1.getText();
        salario_hora = Integer.parseInt(txtSalarioHora.getText());
        horas_mes = Integer.parseInt(txtHorasMes.getText());
        
        salario_mensual = salario_hora * horas_mes;
        
        txtNombre2.setText(String.valueOf(nombre));
        if (salario_mensual > 450000) {
            txtSalario.setText(String.valueOf(salario_mensual));
        } 
    }//GEN-LAST:event_btnCalcularActionPerformed

